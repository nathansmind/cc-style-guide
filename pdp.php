			<h3 id="paragraph">Interesting product Description</h3>
			 
			<p>The Ridley Noah RS brings aero design and wind-cheating technology to the masses. Inspired by the Noah FAST, its geometry and aerodynamic tubing as well as proprietary finish help you stick the break once you make it. And once in the break, the less power you need to maintain speed the more you'll have for the sprint. </p>
			 
			<p>The Noah RS shares the same proven aerodynamic features as its more expensive sibling. An aerodynamic down tube, internal cable routing, an aero seat tube , and deep wheel cutout create the basis of the frame. As to the construction of the frame, it uses a combination of 40t-30t-24t high modulus carbon. Using varying fiber weaves allows engineers to use the best weave for each specific frame application. What sets the Noah apart from other aero bikes is its use of Ridley's FAST Concept technology. FAST Concept technology focuses on specific issues of bicycle aerodynamics. For example, according to Ridley, your wheels produce eight times more drag where they pass through your fork due to the turbulence of the colliding air masses there. The Noah was built to minimize this drag with its use of long, slotted airfoils built into the fork legs, referred to as "F-Splitfork" by Ridley. These airfoils redirect oncoming air away from the turbulent areas near the front wheel. The seatpost is shaped to maximize aerodynamics in the same way as the integrated post of the Noah FASTâ€”the difference is that you won't need to worry about finding a tall enough bike case in which to pack it. And, you won't fret about miss-cutting its height.</p>
			
			<p>Because of structural limitations, parts of the frame are unable to be modified. In these areas, Ridley applied a textured surface treatment for boundary layer control. Ridley calls it "F-Surface," also affectionately referred to as "aero paint." These thin strips of texture are amazing: they purposely trigger a known amount of turbulence that re-energizes lost boundary layers, ultimately re-establishing laminar flow and minimizing drag. And although it seems contrary to physics, extending the contact of air and lengthening the slip-stream helps the frame to cut through air faster.</p>
			 
			<p>The Ridley Noah RS was built using a full complement of SRAM Force 22 components. From the Force 22 Double-tap shifters to the Force 22 derailleur, they provide accurate and fast shifting. To slow you down on those steep descents, Force brakes are employed. The cockpit features Zipp Service Course Handlebars and stem and you'll rest comfortably on a Fi'zi:k Aliante saddle.</p>
			
			<p>The Ridley Noah Road Bike Frame is available in from X-Small to XX-Large and in the colors Black.white/red. Given its semi-compact geometry, we strongly suggest that you focus on the effective top tube length as you make a determination of the proper size.</p>
